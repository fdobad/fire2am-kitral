    def checkMap(self):
        """
        layerComboBox_ignitionPoints <- no need to be AAIGRID
        layerComboBox_ignitionProbMap
        layerComboBox_cbd
        layerComboBox_fuels
        layerComboBox_ccf
        layerComboBox_elevation
        layerComboBox_cbh
        """
        prefix = "layerComboBox_"
        pl = len(prefix)
        for key in filter(lambda key: key[:pl] == prefix, self.dlg.state):
            if layer := self.dlg.state[key]:
                if key[pl:] != "ignitionPoints":
                    ret, val = check_gdal_driver_name(layer, driver_name="AAIGrid")
                    if not ret:
                        nlog(
                            title="Checking Instance",
                            text="Wrong driver for selected layer. Try gdal_translate -of AAIGrid",
                            comboBox=key,
                            layer_name=layer.name(),
                            driver=val,
                            level=2,
                            to_bar=True,
                        )
                        return False
                    if self.W != layer.width():
                        nlog(
                            title="Checking Instance",
                            text="Raster size mismatch",
                            layer_name=layer.name(),
                            layer_width=layer.width(),
                            instance_width=self.W,
                            level=2,
                            to_bar=True,
                        )
                        return False
                    if self.H != layer.height():
                        nlog(
                            title="Checking Instance",
                            text="Raster size mismatch",
                            layer_name=layer.name(),
                            layer_height=layer.height(),
                            instance_height=self.H,
                            level=2,
                            to_bar=True,
                        )
                        return False
                # TODO stop checking CRS
                if layer.crs() == QgsCoordinateReferenceSystem():
                    nlog(
                        title="Checking Instance",
                        text="CRS not set for",
                        layer_name=layer.name(),
                        comboBox=key,
                        level=2,
                        to_bar=True,
                    )
                    return False
                if layer.crs() != self.crs:
                    nlog(
                        title="Checking Instance",
                        text="CRS mismatch",
                        project_crs=self.crs,
                        layer_crs=layer.crs(),
                        comboBox=key,
                        level=2,
                        to_bar=True,
                    )
                    return False
        """ check if ignitionPoints layer has points """
        if self.dlg.state["radioButton_ignitionPoints"]:
            numPoints, msg = checkLayerPoints(self.dlg.state["layerComboBox_ignitionPoints"])
            if numPoints < 1:
                nlog(
                    title="Checking Instance",
                    text=f"Ignition Point problem: {msg}",
                    level=2,
                    to_bar=True,
                )
                return False
        return True


            ''' read dial and slider to generate Weather.csv '''
            nrows = self.dlg.state['spinBox_windConstLen']
            iname = self.project.baseName()
            if iname == '':
                iname = 'Jaime'
            Instance = [ iname ] *nrows
            FireScenario = [ 2 ] *nrows
